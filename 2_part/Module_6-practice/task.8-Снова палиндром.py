# РЕШЕНИЕ
# Пользователь вводит строку. Необходимо написать программу, которая определяет, существует ли у этой строки
# перестановка, при которой она станет палиндромом. Затем она должна выводить соответствующее сообщение.

# Пример 1
#
# Введите строку: aab
#
# Можно сделать палиндромом
#
# Пример 2
#
# Введите строку: aabc

# Нельзя сделать палиндромом
# =============================





# region {===== Основной код =====}

def is_palindrome(s):
    """Проверяет, можно ли переставить символы в строке, чтобы получить палиндром."""
    char_counts = {}
    for char in s:
        if char in char_counts:
            char_counts[char] += 1
        else:
            char_counts[char] = 1

    odd_counts = 0
    for count in char_counts.values():
        if count % 2 != 0:
            odd_counts += 1

    return odd_counts <= 1

string = input("Введите строку: ")
if is_palindrome(string):
    print("Можно сделать палиндромом")
else:
    print("Нельзя сделать палиндромом")

